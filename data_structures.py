# Data Structures
# ===============

# Lists
# Ordered, mutable, and can contain duplicates.
example_list = [1, 2, 3, 4]

# Tuples
# Ordered, immutable, and can contain duplicates.
example_tuple = (1, 2, 3, 4)

# Sets
# Unordered, mutable, and do not contain duplicates.
example_set = {1, 2, 3, 4}

# Dictionaries
# Unordered, mutable, and store key-value pairs. Keys are unique.
example_dict = {"a": 1, "b": 2, "c": 3}

# Strings
# Immutable sequences of characters.
example_string = "Hello, world!"

# Queues (using collections.deque)
# A double-ended queue that allows appending and popping from both ends.
# Example: from collections import deque; q = deque([1, 2, 3])

# Stacks (using lists or collections.deque)
# Last-In-First-Out (LIFO) structure. Can be implemented with lists or deque.
# Example: stack = [1, 2, 3] (push with append() and pop with pop())

# Heaps (using heapq)
# A binary heap implementation for priority queues. Provides efficient access to the smallest element.
# Example: import heapq; heap = [1, 3, 5, 7]; heapq.heapify(heap)

# Linked Lists (using custom classes)
# A collection of nodes where each node points to the next one. Not built-in but can be implemented.
# Example: Implement with a class for nodes and a class for the linked list.

# Trees (using custom classes or libraries like networkx)
# Hierarchical structures. Not built-in but can be implemented or used from libraries.
